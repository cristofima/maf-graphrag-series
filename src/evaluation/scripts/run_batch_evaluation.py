"""
Run batch evaluation with built-in + custom evaluators.

Reads eval_data.jsonl (generated by generate_eval_data.py) and runs
Azure AI Evaluation SDK evaluators to assess agent quality.

Uses Azure OpenAI as LLM-judge for built-in evaluators. Optionally
logs results to Azure AI Foundry for dashboard visualization.

Usage:
    poetry run python -m evaluation.scripts.run_batch_evaluation
    poetry run python -m evaluation.scripts.run_batch_evaluation --foundry  # log to Foundry
"""

from __future__ import annotations

import argparse
import json
import logging
import sys
import time
from datetime import UTC, datetime
from pathlib import Path
from typing import TYPE_CHECKING

from dotenv import load_dotenv

if TYPE_CHECKING:
    from evaluation.config import EvalConfig

load_dotenv()

sys.path.insert(0, str(Path(__file__).resolve().parents[2]))

logger = logging.getLogger(__name__)

DATASETS_DIR = Path(__file__).resolve().parent.parent / "datasets"
EVAL_DATA_PATH = DATASETS_DIR / "eval_data.jsonl"
RESULTS_DIR = Path(__file__).resolve().parent.parent / "results"
DATA_QUERY = "${data.query}"
DATA_RESPONSE = "${data.response}"
DATA_GROUND_TRUTH = "${data.ground_truth}"
AI_SCOPE = "https://ai.azure.com/.default"
EVALS_TIMEOUT_SECONDS = 600
EVALS_POLL_INTERVAL_SECONDS = 5
ITEM_QUERY_TEMPLATE = "{{item.query}}"
ITEM_RESPONSE_TEMPLATE = "{{item.response}}"
ITEM_GROUND_TRUTH_TEMPLATE = "{{item.ground_truth}}"
ITEM_TOOL_DEFINITIONS_TEMPLATE = "{{item.tool_definitions}}"
ITEM_TOOL_CALLS_TEMPLATE = "{{item.tool_calls}}"


def run_batch_evaluation(
    data_path: str | Path = EVAL_DATA_PATH,
    output_dir: str | Path = RESULTS_DIR,
    *,
    use_foundry: bool = False,
    include_custom: bool = True,
) -> dict[str, object]:
    """Run batch evaluation with all evaluators.

    Args:
        data_path: Path to eval_data.jsonl.
        output_dir: Directory for evaluation results.
        use_foundry: If True, log results to Azure AI Foundry dashboard.
        include_custom: If True, include custom GraphRAG evaluators.

    Returns:
        Dict with evaluation metrics and results.
    """
    from azure.ai.evaluation import evaluate

    from evaluation.config import EvalConfig
    from evaluation.evaluators.builtin import create_quality_evaluators

    config = EvalConfig.from_env()
    data_path = Path(data_path)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    if not data_path.exists():
        raise FileNotFoundError(f"Evaluation data not found: {data_path}. Run generate_eval_data.py first.")

    # Built-in quality evaluators (Azure OpenAI as LLM-judge)
    evaluators: dict[str, object] = create_quality_evaluators(config.model_config)

    _, has_structured_tool_calls = _load_new_foundry_rows(data_path)
    if "tool_call_accuracy" in evaluators and not has_structured_tool_calls:
        evaluators.pop("tool_call_accuracy", None)
        logger.warning(
            "Skipping ToolCallAccuracyEvaluator: no structured tool_call items were found in eval_data response payloads."
        )

    # Some newer chat models/deployments reject ``max_tokens`` and require
    # ``max_completion_tokens``. IntentResolutionEvaluator currently sends
    # ``max_tokens`` internally, so we skip it when the deployment is incompatible.
    if "intent_resolution" in evaluators and not _supports_legacy_max_tokens(config):
        evaluators.pop("intent_resolution", None)
        logger.warning(
            "Skipping IntentResolutionEvaluator because deployment '%s' rejects max_tokens.",
            config.eval_chat_deployment,
        )

    _add_custom_evaluators(evaluators, config, include_custom=include_custom)

    # Evaluator config with column mappings
    evaluator_config: dict[str, dict[str, object]] = {
        "task_adherence": {"column_mapping": {"query": DATA_QUERY, "response": DATA_RESPONSE}}
    }

    if "tool_call_accuracy" in evaluators:
        evaluator_config["tool_call_accuracy"] = {
            "column_mapping": {
                "query": DATA_QUERY,
                "response": DATA_RESPONSE,
                "tool_definitions": "${data.tool_definitions}",
            }
        }

    if "intent_resolution" in evaluators:
        evaluator_config["intent_resolution"] = {"column_mapping": {"query": DATA_QUERY, "response": DATA_RESPONSE}}

    if "relevance" in evaluators:
        evaluator_config["relevance"] = {"column_mapping": {"query": DATA_QUERY, "response": DATA_RESPONSE}}

    if "coherence" in evaluators:
        evaluator_config["coherence"] = {"column_mapping": {"query": DATA_QUERY, "response": DATA_RESPONSE}}

    if "response_completeness" in evaluators:
        evaluator_config["response_completeness"] = {
            "column_mapping": {"ground_truth": DATA_GROUND_TRUTH, "response": DATA_RESPONSE}
        }

    # Custom evaluators use direct response text
    if "entity_accuracy" in evaluators:
        evaluator_config["entity_accuracy"] = {"column_mapping": {"response": DATA_RESPONSE}}
    if "relationship_validity" in evaluators:
        evaluator_config["relationship_validity"] = {"column_mapping": {"response": DATA_RESPONSE}}

    selected_foundry_evaluators = {
        name
        for name in (
            "task_adherence",
            "intent_resolution",
            "relevance",
            "coherence",
            "response_completeness",
            "tool_call_accuracy",
        )
        if name in evaluators
    }

    # Evaluate kwargs
    evaluate_kwargs: dict[str, object] = {
        "data": str(data_path),
        "evaluators": evaluators,
        "evaluator_config": evaluator_config,
        "output_path": str(output_dir / "evaluation_results.json"),
    }

    new_foundry_run: dict[str, object] | None = None
    if use_foundry:
        new_foundry_run = _publish_new_foundry_batch_run(
            data_path=data_path,
            config=config,
            evaluator_names=selected_foundry_evaluators,
        )

    logger.info("Running batch evaluation on %s", data_path)
    result = evaluate(**evaluate_kwargs)

    if new_foundry_run:
        result["new_foundry"] = new_foundry_run
        report_url = new_foundry_run.get("report_url")
        if isinstance(report_url, str) and report_url:
            result["studio_url"] = report_url

    # Write summary report
    _write_report(result, output_dir / "evaluation_report.md")

    return result


def _write_report(result: dict[str, object], output_path: Path) -> None:
    """Write a Markdown evaluation report."""
    metrics = result.get("metrics", {})
    studio_url = result.get("studio_url", "")

    lines = [
        "# Evaluation Report",
        "",
        "## Summary Metrics",
        "",
        "| Evaluator | Score |",
        "|-----------|-------|",
    ]

    for name, value in sorted(metrics.items()):
        lines.append(f"| {name} | {value} |")

    lines.append("")

    if studio_url:
        lines.append("## Azure AI Foundry Dashboard")
        lines.append("")
        lines.append(f"[View detailed results]({studio_url})")
        lines.append("")

    lines.append("---")
    lines.append("*Generated by `evaluation.scripts.run_batch_evaluation`*")

    output_path.write_text("\n".join(lines), encoding="utf-8")
    logger.info("Report written to %s", output_path)


def _extract_response_text(response: object) -> str:
    """Extract assistant text from structured response payloads.

    Args:
        response: Response field loaded from JSONL evaluation data.

    Returns:
        Plain text suitable for New Foundry evaluators.
    """
    if isinstance(response, str):
        return response

    if isinstance(response, dict):
        content = response.get("content", "")
        return _extract_text_from_content(content)

    if isinstance(response, list):
        assistant_text = _extract_last_assistant_text(response)
        if assistant_text:
            return assistant_text

        try:
            return json.dumps(response, ensure_ascii=False)
        except TypeError:
            return str(response)

    return str(response)


def _extract_text_from_content(content: object) -> str:
    """Extract text from OpenAI-style message content."""
    if isinstance(content, str):
        return content

    if not isinstance(content, list):
        return str(content)

    chunks: list[str] = []
    for item in content:
        if isinstance(item, str):
            chunks.append(item)
            continue

        if isinstance(item, dict):
            if item.get("type") in {"text", "input_text", "output_text"} and isinstance(item.get("text"), str):
                chunks.append(item["text"])
                continue

            if isinstance(item.get("tool_result"), str):
                chunks.append(item["tool_result"])

    return "\n".join(chunk for chunk in chunks if chunk).strip()


def _extract_last_assistant_text(messages: list[object]) -> str:
    """Extract the most recent assistant text from message history."""
    for message in reversed(messages):
        if not isinstance(message, dict):
            continue

        if str(message.get("role", "")).lower() != "assistant":
            continue

        text = _extract_text_from_content(message.get("content", ""))
        if text:
            return text

    return ""


def _extract_tool_calls(response: object) -> list[dict[str, object]]:
    """Extract OpenAI-style tool_call entries from structured response payloads."""
    if not isinstance(response, list):
        return []

    tool_calls: list[dict[str, object]] = []

    for message in response:
        if not isinstance(message, dict):
            continue

        content = message.get("content")
        if not isinstance(content, list):
            continue

        for item in content:
            if not isinstance(item, dict):
                continue

            if item.get("type") != "tool_call":
                continue

            tool_call = item.get("tool_call")
            if isinstance(tool_call, dict):
                tool_calls.append(tool_call)

    return tool_calls


def _load_new_foundry_rows(data_path: Path) -> tuple[list[dict[str, object]], bool]:
    """Load JSONL records and normalize them for New Foundry evals."""
    rows: list[dict[str, object]] = []
    has_structured_tool_calls = False

    with data_path.open(encoding="utf-8") as file_handle:
        for line in file_handle:
            stripped = line.strip()
            if not stripped:
                continue

            entry = json.loads(stripped)
            query = str(entry.get("query", "")).strip()
            if not query:
                continue

            row = {
                "query": query,
                "response": _extract_response_text(entry.get("response", "")),
                "ground_truth": str(entry.get("ground_truth", "")),
                "tool_definitions": entry.get("tool_definitions", []),
                "tool_calls": _extract_tool_calls(entry.get("response", [])),
            }

            if row["tool_calls"]:
                has_structured_tool_calls = True

            rows.append(row)

    if not rows:
        raise ValueError(f"No evaluation rows were loaded from {data_path}")

    return rows, has_structured_tool_calls


def _build_new_foundry_testing_criteria(
    evaluator_names: set[str],
    model_deployment: str,
    *,
    has_structured_tool_calls: bool,
) -> list[dict[str, object]]:
    """Build testing_criteria payload for New Foundry evals API."""
    criteria: list[dict[str, object]] = []

    if "task_adherence" in evaluator_names:
        criteria.append(
            {
                "type": "azure_ai_evaluator",
                "name": "task_adherence",
                "evaluator_name": "builtin.task_adherence",
                "initialization_parameters": {"deployment_name": model_deployment},
                "data_mapping": {"query": ITEM_QUERY_TEMPLATE, "response": ITEM_RESPONSE_TEMPLATE},
            }
        )

    if "intent_resolution" in evaluator_names:
        criteria.append(
            {
                "type": "azure_ai_evaluator",
                "name": "intent_resolution",
                "evaluator_name": "builtin.intent_resolution",
                "initialization_parameters": {"deployment_name": model_deployment},
                "data_mapping": {"query": ITEM_QUERY_TEMPLATE, "response": ITEM_RESPONSE_TEMPLATE},
            }
        )

    if "tool_call_accuracy" in evaluator_names and has_structured_tool_calls:
        criteria.append(
            {
                "type": "azure_ai_evaluator",
                "name": "tool_call_accuracy",
                "evaluator_name": "builtin.tool_call_accuracy",
                "initialization_parameters": {"deployment_name": model_deployment},
                "data_mapping": {
                    "query": ITEM_QUERY_TEMPLATE,
                    "tool_calls": ITEM_TOOL_CALLS_TEMPLATE,
                    "tool_definitions": ITEM_TOOL_DEFINITIONS_TEMPLATE,
                },
            }
        )

    if "relevance" in evaluator_names:
        criteria.append(
            {
                "type": "azure_ai_evaluator",
                "name": "relevance",
                "evaluator_name": "builtin.relevance",
                "initialization_parameters": {"deployment_name": model_deployment},
                "data_mapping": {
                    "query": ITEM_QUERY_TEMPLATE,
                    "response": ITEM_RESPONSE_TEMPLATE,
                },
            }
        )

    if "coherence" in evaluator_names:
        criteria.append(
            {
                "type": "azure_ai_evaluator",
                "name": "coherence",
                "evaluator_name": "builtin.coherence",
                "initialization_parameters": {"deployment_name": model_deployment},
                "data_mapping": {
                    "query": ITEM_QUERY_TEMPLATE,
                    "response": ITEM_RESPONSE_TEMPLATE,
                },
            }
        )

    if "response_completeness" in evaluator_names:
        criteria.append(
            {
                "type": "azure_ai_evaluator",
                "name": "response_completeness",
                "evaluator_name": "builtin.response_completeness",
                "initialization_parameters": {"deployment_name": model_deployment},
                "data_mapping": {
                    "ground_truth": ITEM_GROUND_TRUTH_TEMPLATE,
                    "response": ITEM_RESPONSE_TEMPLATE,
                },
            }
        )

    return criteria


def _publish_new_foundry_batch_run(
    data_path: Path,
    config: EvalConfig,
    evaluator_names: set[str],
) -> dict[str, object]:
    """Publish an evaluation run to New Foundry via openai/v1/evals.

    Args:
        data_path: Path to evaluation JSONL dataset.
        config: Evaluation configuration loaded from environment.
        evaluator_names: Built-in evaluator names selected for local run.

    Returns:
        Metadata for the New Foundry run, including nextgen report URL.

    Raises:
        RuntimeError: If New Foundry publish fails.
    """
    import httpx
    from azure.identity import DefaultAzureCredential

    if not config.azure_ai_project:
        raise RuntimeError("--foundry requires AZURE_AI_PROJECT to publish New Foundry runs.")

    rows, has_structured_tool_calls = _load_new_foundry_rows(data_path)
    testing_criteria = _build_new_foundry_testing_criteria(
        evaluator_names,
        model_deployment=config.eval_chat_deployment,
        has_structured_tool_calls=has_structured_tool_calls,
    )

    if "tool_call_accuracy" in evaluator_names and not has_structured_tool_calls:
        logger.warning(
            "Skipping New Foundry tool_call_accuracy: no structured tool_call items were found in eval_data response payloads."
        )
    base_url = f"{config.azure_ai_project.rstrip('/')}/openai/v1"
    eval_name = f"graphrag-batch-{datetime.now(UTC).strftime('%Y%m%d-%H%M%S')}"

    credential = DefaultAzureCredential()
    token = credential.get_token(AI_SCOPE).token
    headers = {"Authorization": f"Bearer {token}"}

    eval_payload: dict[str, object] = {
        "name": eval_name,
        "data_source_config": {
            "type": "custom",
            "item_schema": {
                "type": "object",
                "properties": {
                    "query": {"type": "string"},
                    "response": {"type": "string"},
                    "ground_truth": {"type": "string"},
                    "tool_definitions": {"type": "array"},
                    "tool_calls": {"type": "array"},
                },
                "required": ["query", "response", "ground_truth"],
            },
        },
        "testing_criteria": testing_criteria,
    }

    run_payload = {
        "name": f"{eval_name}-run",
        "data_source": {
            "type": "jsonl",
            "source": {"type": "file_content", "content": [{"item": row} for row in rows]},
        },
    }

    with httpx.Client(timeout=60.0, headers=headers) as client:
        eval_response = client.post(f"{base_url}/evals", json=eval_payload)
        eval_response.raise_for_status()
        eval_body = eval_response.json()
        eval_id = str(eval_body["id"])

        run_response = client.post(f"{base_url}/evals/{eval_id}/runs", json=run_payload)
        run_response.raise_for_status()
        run_body = run_response.json()
        run_id = str(run_body["id"])

        deadline = time.monotonic() + EVALS_TIMEOUT_SECONDS
        latest_run = run_body

        while time.monotonic() < deadline:
            status = str(latest_run.get("status", "")).lower()
            if status in {"completed", "failed", "canceled"}:
                break

            time.sleep(EVALS_POLL_INTERVAL_SECONDS)
            poll_response = client.get(f"{base_url}/evals/{eval_id}/runs/{run_id}")
            poll_response.raise_for_status()
            latest_run = poll_response.json()

        final_status = str(latest_run.get("status", "")).lower()
        error_message = ""
        if isinstance(latest_run.get("error"), dict):
            error_message = str(latest_run["error"].get("message", ""))

        if final_status != "completed":
            raise RuntimeError(
                "New Foundry batch run did not complete successfully "
                f"(status={final_status or 'unknown'}). {error_message}".strip()
            )

        report_url = str(latest_run.get("report_url", ""))
        logger.info("New Foundry batch run completed: %s", report_url)

        return {
            "eval_id": eval_id,
            "run_id": run_id,
            "status": latest_run.get("status", "completed"),
            "report_url": report_url,
        }


def _supports_legacy_max_tokens(config: EvalConfig) -> bool:
    """Check whether the configured deployment accepts ``max_tokens``.

    IntentResolutionEvaluator currently depends on this parameter.
    """
    from openai import AzureOpenAI

    client = AzureOpenAI(
        azure_endpoint=config.azure_endpoint,
        api_key=config.api_key,
        api_version=config.api_version,
    )

    try:
        client.chat.completions.create(
            model=config.eval_chat_deployment,
            messages=[{"role": "user", "content": "ping"}],
            max_tokens=1,
        )
        return True
    except Exception as exc:  # pragma: no cover - integration behavior depends on model/runtime
        message = str(exc)
        if "max_tokens" in message and "max_completion_tokens" in message:
            return False

        logger.warning(
            "Could not determine max_tokens support for deployment '%s'; keeping IntentResolutionEvaluator. Error: %s",
            config.eval_chat_deployment,
            exc,
        )
        return True


def _add_custom_evaluators(evaluators: dict[str, object], config: EvalConfig, *, include_custom: bool) -> None:
    """Add optional graph-based evaluators that read local Parquet outputs."""
    if not include_custom:
        return

    entities_path = _resolve_parquet_path(
        Path(config.entities_parquet_path),
        fallback_name="entities.parquet",
    )
    relationships_path = _resolve_parquet_path(
        Path(config.relationships_parquet_path),
        fallback_name="relationships.parquet",
    )

    if entities_path.exists():
        try:
            from evaluation.evaluators.entity_accuracy import EntityAccuracyEvaluator

            evaluators["entity_accuracy"] = EntityAccuracyEvaluator(str(entities_path))
            logger.info("Added EntityAccuracyEvaluator")
        except Exception as exc:  # pragma: no cover - depends on local parquet shape
            logger.warning("Skipping EntityAccuracyEvaluator due to initialization error: %s", exc)

    if relationships_path.exists() and entities_path.exists():
        try:
            from evaluation.evaluators.relationship_validity import RelationshipValidityEvaluator

            evaluators["relationship_validity"] = RelationshipValidityEvaluator(str(relationships_path), str(entities_path))
            logger.info("Added RelationshipValidityEvaluator")
        except Exception as exc:  # pragma: no cover - depends on local parquet shape
            logger.warning("Skipping RelationshipValidityEvaluator due to initialization error: %s", exc)


def _resolve_parquet_path(preferred_path: Path, *, fallback_name: str) -> Path:
    """Resolve graph parquet file path with a backward-compatible fallback.

    GraphRAG output names changed across versions. We first try the configured
    path, then fall back to the same directory with the newer filename.
    """
    if preferred_path.exists():
        return preferred_path

    fallback_path = preferred_path.parent / fallback_name
    if fallback_path.exists():
        logger.info("Using fallback parquet path: %s", fallback_path)
        return fallback_path

    logger.warning("Parquet path not found: %s", preferred_path)
    return preferred_path


